# Python Type Casting
'''Type casting refers to converting an object of one type into another.'''

# Python Implicit Casting
'''When any language compiler/interpreter automtically converts object of one type into other, it is called automatic or implicit casting.'''

a = True;
b = 10.5;
c = a+b;

print(c);   # Here output will be 11.5, True is equal to 1 and false is equal to 0.

# Python Explicit Casting
'''You can use Python's  build-in function int(), float(), and str() to perform the explicit conversions such as string to integer.'''

# Convert number, float and string into data type:

a = int(1)  # a will be 1
b = int(2.2)  # b wil be 2
c = int("3")   # c will be 3

print(a)
print(b)
print(c)


# Python str() Function

a = str(1)  # a will be "1"
b = str(2.2)   # b will be "2.2"
c = str("3.3")  # c will be "3.3"

print(a)
print(b)
print(c)