# Python - Update Tuples

# Original tuple
T1 = (10, 20, 30, 40)
# Tuple to be concatenated
T2 = ('one', 'two', 'three', 'four')
# Updating the tuple using the concatenation operator
T1 = T1 + T2
print(T1)


# Python - Unpack Tuple Items

'''Unpack Tuple Items
The term "unpacking" refers to the process of parsing tuple items in individual variables.'''

# Example
tup1 = (10,20,30)
x, y, z = tup1
print ("x: ", x, "y: ", "z: ",z)